class TransmissionEntity {
  final String image;
  final String photo;
  final String title;
  final String subtitle;
  final String source;
  final String time;
  final String description;
  final String link;

  const TransmissionEntity({
    required this.image,
    required this.photo,
    required this.title,
    required this.subtitle,
    required this.source,
    required this.time,
    required this.description,
    required this.link,
  });
}
