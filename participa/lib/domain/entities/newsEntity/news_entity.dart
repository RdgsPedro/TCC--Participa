class NewsEntity {
  final String image;
  final String title;
  final String tag;
  final String source;
  final String time;
  final String description;
  final String link;

  const NewsEntity({
    required this.image,
    required this.title,
    required this.tag,
    required this.source,
    required this.time,
    required this.description,
    required this.link,
  });
}
